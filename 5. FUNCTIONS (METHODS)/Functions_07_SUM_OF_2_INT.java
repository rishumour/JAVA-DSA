// Calculates and returns the sum of two integers

public class Functions_07_SUM_OF_2_INT {

    public static int sum(int a, int b) {
        int sum = a + b;
        return sum;
    }
    public static void main(String[] args) {
        int a = 5, b = 5;
        System.out.println("sum = " + sum(a, b));
    }
}